    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        
        if(jTextField1.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "No ha ingresado un tipo de gasto para buscar");
        }
        else
        {
            String[] gastos = {"id_tipo_gasto, tipo_gasto,limite_gasto,acumulado_a_fecha,diferencia"};
            Object[][] gasto =control.getSentencia().GetTabla(gastos, "gastos_personales", "select * from gastos_personales where tipo_gasto='"+jTextField1.getText()+"'");
            jTextField2.setText(gasto[0][2].toString());
            jTextField3.setText(gasto[0][3].toString());
            jTextField4.setText(gasto[0][4].toString());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

